package com.hover.iot.enumeration;

/**
 * An enumeration representing commonly found consumer device types.
 * <li>LIGHT</li>
 * <li>SWITCH</li>
 * <li>SENSOR</li>
 * <li>LOCK</li>
 * <li>PLUG</li>
 * <li>TV</li>
 * <li>PC</li>
 * <li>AIR_PURIFIER</li>
 * <li>BLINDS</li>
 */
public enum DeviceType {
    /**
     * Represents a light.
     */
    LIGHT,
    /**
     * Represents a switch.
     */
    SWITCH,
    /**
     * Represents a sensor.
     */
    SENSOR,
    /**
     * Represents a lock.
     */
    LOCK,
    /**
     * Represents a plug.
     */
    PLUG,
    /**
     * Represents a tv.
     */
    TV,
    /**
     * Represents a pc.
     */
    PC,
    /**
     *  Represents an air purifier.
     */
    AIR_PURIFIER,
    /**
     *  Represents blinds or shades.
     */
    BLINDS,
    /**
     *  Represents a kettle.
     */
    KETTLE
}
