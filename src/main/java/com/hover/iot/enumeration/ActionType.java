package com.hover.iot.enumeration;

/**
 * An enumeration representing the types of automation rules.
 * <li>ATTRIBUTE</li>
 * <li>SCENE</li>
 * <li>NOTIFICATION</li>
 */
public enum ActionType {
    /**
     * Represents an action related to an attribute.
     */
    ATTRIBUTE,

    /**
     * Represents a notification action.
     */
    SCENE,

    /**
     * Represents a notification action.
     */
    NOTIFICATION,
}