package com.hover.iot.enumeration;

/**
 * An enumeration representing the different device network communication.
 * <li>HTTP</li>
 * <li>MQTT</li>
 * <li>ZIGBEE</li>
 * <li>BLUETOOTH</li>
 */
public enum NetworkType {
    /**
     * Represents a Http network communication type.
     */
    HTTP,
    /**
     * Represents a Mqtt network communication type.
     */
    MQTT,
    /**
     * Represents a Zigbee network communication type.
     */
    ZIGBEE,
    /**
     * Represents a Bluetooth network communication type.
     */
    BLUETOOTH
}
