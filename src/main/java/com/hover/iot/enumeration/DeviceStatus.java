package com.hover.iot.enumeration;

/**
 * An enumeration representing the status of a device.
 * <li>ONLINE</li>
 * <li>OFFLINE</li>
 * <li>ERROR</li>
 */
public enum DeviceStatus {

    /**
     * Represents device that is online.
     */
    ONLINE,

    /**
     * Represents a device that is offline.
     */
    OFFLINE,

    /**
     * Represents a device that encountered an error or malfunction.
     */
    ERROR,
}
