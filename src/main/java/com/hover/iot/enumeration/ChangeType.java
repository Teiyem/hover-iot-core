package com.hover.iot.enumeration;

/**
 * An enumeration representing the entity change types in the application.
 * <li>ADDED</li>
 * <li>DELETED</li>
 * <li>MODIFIED</li>
 */
public enum ChangeType {
    /**
     * Represents an entity being added.
     */
    ADDED,
    /**
     * Represents an entity being deleted.
     */
    DELETED,
    /**
     * Represents an entity being modified.
     */
    MODIFIED,
}
