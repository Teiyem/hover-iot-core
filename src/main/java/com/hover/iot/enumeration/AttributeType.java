package com.hover.iot.enumeration;

/**
 * An enumeration representing the types of values that can be stored in an attribute.
 * <li>STRING</li>
 * <li>DOUBLE</li>
 * <li>INTEGER</li>
 * <li>BOOLEAN</li>
 */
public enum AttributeType {
    /**
     * Represents a String type.
     */
    STRING,
    /**
     * Represents a Float type.
     */
    FLOAT,
    /**
     * Represents a long type.
     */
    LONG,
    /**
     * Represents an Integer type.
     */
    INTEGER,
    /**
     * Represents a Boolean type.
     */
    BOOLEAN,
}